#include "ft_printf.h"

int	ft_putchar(char c)
{
	return (write(1, &c, 1));
}

int	ft_putstr(char *s)
{
	int	len;

	len = 0;
	while (*s)
	{
		ft_putchar(*s);
		len++;
		s++;
	}
	return (len);
}
